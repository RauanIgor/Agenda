    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        String s_cpf = rcpf.getText();
        String s_nome = rnome.getText();
        String s_senha = new String(rsenha.getPassword());
        String s_confSenha = new String(rconfirmasenha.getPassword());
        if (!s_cpf.equals(" ") && !s_nome.equals(" ") && !s_senha.equals(" ") && !s_confSenha.equals(" ")) {
            if (s_senha.equals(s_confSenha)) {
                if(!Usuario.existe(s_cpf)){
                    Usuario u = new Usuario(s_cpf, s_nome, s_senha);
                    u.salvar();
                    JOptionPane.showMessageDialog(this, "Conta criada");
                    rcpf.setText("");
                    rnome.setText("");
                    rsenha.setText("");
                    rconfirmasenha.setText("");
                    
                }else{
                    JOptionPane.showMessageDialog(this, "CPF j√° cadastrado");
                }
            } else {
                JOptionPane.showMessageDialog(this, "As senhas devem ser  iguais ");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos ");
        }
    }//GEN-LAST:event_cadastrarActionPerformed

