    private void entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarActionPerformed
        String s_cpf = cpf.getText();
        String s_senha = new String(senha.getPassword());
        if(!s_cpf.equals("") && !s_senha.equals("")){
            Usuario.usuario = Usuario.autenticar(s_cpf, s_senha);
            if(Usuario.usuario != null){
                new Main().setVisible(true);
                dispose();
                
                
            }else{
                JOptionPane.showMessageDialog(this, "CPF e/ou senha inv√°lidos");
            }
        
        }else{
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!");
        }
    }//GEN-LAST:event_entrarActionPerformed

